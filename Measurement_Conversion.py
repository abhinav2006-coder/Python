inch = float(input("Enter the length in inches: "))

# Conversion from inches to feet
foot = inch / 12
print(inch,"inches is equal to ",foot," feet.")

# Conversion from inches to yards
yard = inch / 36
print(inch,"inches is equal to ",yard," yards.")

# Conversion from inches to centimeters
centimeter = inch * 2.54
print(inch,"inches is equal to ",centimeter," centimeters.")

# Conversion from inches to meters
meter = inch * 0.0254
print(inch,"inches is equal to ",meter," meters.")